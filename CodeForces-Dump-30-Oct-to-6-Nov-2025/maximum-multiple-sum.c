int main(void) {
    int t;
    scanf("%d", &t);
    while(t--) {
        int n;
        scanf("%d", &n);
        if(n == 2 || n == 3) printf("%d\n", n);
        else if(n > 3) printf("2\n");
    }
    return 0;
}


